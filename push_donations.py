def parse_date(d):
    """Convert d.m.yy or d/m/yy  →  yyyy-mm-dd"""
    d = d.replace("/", ".")
    parts = d.split(".")
    day, mon, yr = int(parts[0]), int(parts[1]), int(parts[2])
    yr = 2000 + yr if yr < 100 else yr
    return f"{yr:04d}-{mon:02d}-{day:02d}"

